function bobFrame(rows){
  const blank='.'.repeat(rows[0].length);
  return [blank].concat(rows.slice(0,rows.length-1));
}
function terrPix(t,x,y,seed){
  // Шум террейна для тайлов 28×32 (двойная плотность против старых 14×16).
  const h=hash2(x,y,seed);
  switch(t){
    case T.WATER: if(h<0.035)return PAL.W3; if(h>0.95)return PAL.W1; return PAL.W2;
    case T.GRASS: if(h<0.05)return PAL.G3; if(h>0.94)return PAL.G1; return PAL.G2;
    case T.FOREST:{const b=hash2(x>>2,y>>2,seed+3);if(b<0.28)return PAL.F1;if(b>0.74&&h<0.5)return PAL.F3;return PAL.F2}
    case T.ROCK: if(h<0.07)return PAL.R1; if(h>0.93)return PAL.R3; return PAL.R2;
    case T.MTN:{const d=(x+y)&15;let c=(d<6)?PAL.M2:PAL.M1;if(h<0.05)c=PAL.M1;return c}
    // снег гор — в decorTri (19_atlas) и в снежном поясе E>=4.5 (rl5), не в шуме
  }
  return '#f0f';
}
